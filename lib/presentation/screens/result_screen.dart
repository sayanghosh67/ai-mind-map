import 'package:flutter/material.dart';
import 'package:flutter_riverpod/flutter_riverpod.dart';
import 'package:screenshot/screenshot.dart';
import 'package:share_plus/share_plus.dart';
import 'dart:typed_data';
import 'package:pdf/pdf.dart';
import 'package:pdf/widgets.dart' as pw;
import '../../domain/providers/app_providers.dart';
import '../widgets/mind_map_widget.dart';

class ResultScreen extends ConsumerStatefulWidget {
  const ResultScreen({super.key});

  @override
  ConsumerState<ResultScreen> createState() => _ResultScreenState();
}

class _ResultScreenState extends ConsumerState<ResultScreen> {
  final ScreenshotController screenshotController = ScreenshotController();
  bool isExporting = false;

  Future<void> _shareMindMap(bool asPdf) async {
    if (isExporting) return;
    
    setState(() => isExporting = true);
    try {
      // capture with a longer delay to ensure all nodes are rendered and animated in
      final Uint8List? imageBytes = await screenshotController.capture(
        delay: const Duration(milliseconds: 500),
        pixelRatio: 2.0, // Higher quality
      );
      
      if (imageBytes != null) {
        if (asPdf) {
          final pdf = pw.Document();
          final image = pw.MemoryImage(imageBytes);

          pdf.addPage(
            pw.Page(
              pageFormat: PdfPageFormat.a4,
              build: (pw.Context context) {
                return pw.Center(
                  child: pw.Image(image),
                );
              },
            ),
          );

          final pdfBytes = await pdf.save();
          final String fileName = 'mindmap_${DateTime.now().millisecondsSinceEpoch}.pdf';

          if (mounted) {
            await Share.shareXFiles(
              [
                XFile.fromData(
                  pdfBytes,
                  name: fileName,
                  mimeType: 'application/pdf',
                )
              ], 
              text: 'Check out this Mind Map generated by AI!'
            );
          }
        } else {
          final String fileName = 'mindmap_${DateTime.now().millisecondsSinceEpoch}.png';

          if (mounted) {
            await Share.shareXFiles(
              [
                XFile.fromData(
                  imageBytes,
                  name: fileName,
                  mimeType: 'image/png',
                )
              ], 
              text: 'Check out this Mind Map generated by AI!'
            );
          }
        }
      } else {
        throw Exception('Could not capture screenshot of the mind map.');
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to share: $e'),
            backgroundColor: Theme.of(context).colorScheme.error,
          ),
        );
      }
    } finally {
      if (mounted) setState(() => isExporting = false);
    }
  }

  @override
  Widget build(BuildContext context) {
    final mindMapAsync = ref.watch(mindMapProvider);
    final theme = Theme.of(context);

    return Scaffold(
      appBar: AppBar(
        title: const Text('Interactive Mind Map', style: TextStyle(fontWeight: FontWeight.bold)),
        actions: [
          if (isExporting)
            const Padding(
              padding: EdgeInsets.all(16.0),
              child: SizedBox(
                width: 24,
                height: 24,
                child: CircularProgressIndicator(strokeWidth: 2),
              ),
            )
          else ...[
            IconButton(
              icon: const Icon(Icons.image),
              onPressed: () => _shareMindMap(false),
              tooltip: 'Share as Image',
            ),
            IconButton(
              icon: const Icon(Icons.picture_as_pdf),
              onPressed: () => _shareMindMap(true),
              tooltip: 'Share as PDF',
            ),
          ],
        ],
      ),
      body: mindMapAsync.when(
        data: (mindMapNode) {
          return Screenshot(
            controller: screenshotController,
            child: Container(
              color: theme.colorScheme.background, // Ensure background is captured
              child: MindMapWidget(rootNode: mindMapNode),
            ),
          );
        },
        loading: () => Center(
          child: Column(
            mainAxisAlignment: MainAxisAlignment.center,
            children: [
              CircularProgressIndicator(color: theme.colorScheme.primary),
              const SizedBox(height: 24),
              Text(
                'AI is generating your mind map...',
                style: theme.textTheme.titleLarge,
              ),
            ],
          ),
        ),
        error: (error, stack) => Center(
          child: Padding(
            padding: const EdgeInsets.all(32.0),
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                Icon(
                  Icons.error_outline,
                  size: 64,
                  color: theme.colorScheme.error,
                ),
                const SizedBox(height: 24),
                Text(
                  'Failed to generate mind map',
                  style: theme.textTheme.titleLarge?.copyWith(
                    color: theme.colorScheme.error,
                  ),
                ),
                const SizedBox(height: 16),
                Text(
                  error.toString(),
                  textAlign: TextAlign.center,
                  style: theme.textTheme.bodyMedium,
                ),
                const SizedBox(height: 32),
                ElevatedButton(
                  onPressed: () {
                    ref.invalidate(mindMapProvider);
                  },
                  child: const Text('Try Again'),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
